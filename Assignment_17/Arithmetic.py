#Create one module named Arithmetic which contains four functions: Add(), Sub(), Mult(), and Div(). All functions accept two numbers as parameters and perform the respective operation. Write a Python program that calls all these functions by accepting parameters from the user.


def Add(no1, no2):
    return no1 + no2

def Sub(no1, no2):
    return no1 - no2

def Mult(no1, no2):
    return no1 * no2

def Div(no1, no2):
    return no1 / no2